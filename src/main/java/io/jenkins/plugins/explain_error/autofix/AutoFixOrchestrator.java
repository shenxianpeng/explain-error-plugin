    /**
     * Attempts to automatically fix the build failure described by {@code errorLogs}.
     *
     * @param run                 the failing build run
     * @param errorLogs           the error logs to analyse
     * @param aiProvider          the configured AI provider
     * @param credentialsId       Jenkins credentials ID for SCM token
     * @param remoteUrl           explicit SCM remote URL; if null or blank, extracted from the job's SCM config
     * @param scmTypeOverride     optional override for SCM type detection ("github"/"gitlab"/"bitbucket")
     * @param githubEnterpriseUrl optional GitHub Enterprise API base URL (e.g. https://ghe.example.com)
     * @param gitlabUrl           optional self-hosted GitLab base URL
     * @param bitbucketUrl        optional self-hosted Bitbucket base URL
     * @param allowedPathGlobs    list of glob patterns that restrict which files may be changed
     * @param draftPr             whether to open a draft PR
     * @param timeoutSeconds      overall timeout in seconds
     * @param listener            task listener for build-log output
     * @return the result of the auto-fix attempt
     */
    public AutoFixResult attemptAutoFix(
            Run<?, ?> run,
            String errorLogs,
            BaseAIProvider aiProvider,
            String credentialsId,
            String remoteUrl,
            String scmTypeOverride,
            String githubEnterpriseUrl,
            String gitlabUrl,
            String bitbucketUrl,
            List<String> allowedPathGlobs,
            boolean draftPr,
            int timeoutSeconds,
            TaskListener listener,
            String prTemplate) {

        // We keep track of the branch name so we can clean up on timeout/failure.
        final String[] createdBranchRef = {null};

        CompletableFuture<AutoFixResult> future = CompletableFuture.supplyAsync(() -> {
            try {
                return doAttemptAutoFix(
                        run, errorLogs, aiProvider, credentialsId, remoteUrl,
                        scmTypeOverride, githubEnterpriseUrl, gitlabUrl, bitbucketUrl,
                        allowedPathGlobs, draftPr, listener, createdBranchRef, prTemplate);
            } catch (Exception e) {
                LOGGER.log(Level.WARNING, "Auto-fix failed with exception", e);
                listener.getLogger().println("[AutoFix] Error: " + e.getMessage());
                return AutoFixResult.failed("Auto-fix encountered an unexpected error: " + e.getMessage());
            }
        }, EXECUTOR);

        try {
            return future.get(timeoutSeconds, TimeUnit.SECONDS);
        } catch (TimeoutException e) {
            future.cancel(true);
            listener.getLogger().println("[AutoFix] Timed out after " + timeoutSeconds + " seconds.");
            // Best-effort branch cleanup
            if (createdBranchRef[0] != null) {
                try {
                    String resolvedUrl = (remoteUrl != null && !remoteUrl.isBlank())
                            ? remoteUrl : extractRemoteUrl(run);
                    String token = resolveScmToken(credentialsId, run);
                    if (token != null) {
                        ScmRepo repo = buildScmRepo(resolvedUrl, token,
                                scmTypeOverride, githubEnterpriseUrl, gitlabUrl, bitbucketUrl);
                        ScmApiClient client = ScmClientFactory.create(repo);
                        client.deleteBranch(createdBranchRef[0]);
                    }
                } catch (Exception ex) {
                    LOGGER.log(Level.WARNING, "Failed to clean up branch after timeout", ex);
                }
            }
            return AutoFixResult.timedOut();
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
            return AutoFixResult.failed("Auto-fix interrupted.");
        } catch (ExecutionException e) {
            Throwable cause = e.getCause();
            LOGGER.log(Level.WARNING, "Auto-fix execution failed", cause);
            return AutoFixResult.failed("Auto-fix execution failed: " + (cause != null ? cause.getMessage() : e.getMessage()));
        }
    }

    // -------------------------------------------------------------------------
    // Core logic
    // -------------------------------------------------------------------------

    private AutoFixResult doAttemptAutoFix(
            Run<?, ?> run,
            String errorLogs,
            BaseAIProvider aiProvider,
            String credentialsId,
            String remoteUrl,
            String scmTypeOverride,
            String githubEnterpriseUrl,
            String gitlabUrl,
            String bitbucketUrl,
            List<String> allowedPathGlobs,
            boolean draftPr,
            TaskListener listener,
            String[] createdBranchRef,
            String prTemplate) throws Exception {

        listener.getLogger().println("[AutoFix] Requesting fix suggestion from AI provider...");

        // Early validation — fail before spending AI tokens
        if (credentialsId == null || credentialsId.isBlank()) {
            return AutoFixResult.failed("autoFixCredentialsId is required for auto-fix");
        }

        // Step 1 — Get AI fix suggestion
        FixAssistant fixAssistant = aiProvider.createFixAssistant(run.getParent(), null);
        String rawJson = fixAssistant.suggestFix(errorLogs);
        LOGGER.fine("Raw AI response: " + rawJson);
        FixSuggestion suggestion = parseFixSuggestion(rawJson);

        listener.getLogger().println("[AutoFix] AI suggestion: fixable=" + suggestion.fixable()
                + ", confidence=" + suggestion.confidence()
                + ", fixType=" + suggestion.fixType());

        // Step 2 — Check fixability
        if (!suggestion.fixable() || "low".equals(suggestion.confidence())) {
            listener.getLogger().println("[AutoFix] Skipping: AI confidence too low or not fixable.");
            return AutoFixResult.skippedLowConfidence();
        }

        if (suggestion.changes() == null || suggestion.changes().isEmpty()) {
            listener.getLogger().println("[AutoFix] Skipping: AI returned fixable=true but no file changes.");
            return AutoFixResult.skippedLowConfidence();
        }

        // Step 3 — Validate file paths
        if (suggestion.changes() != null) {
            for (FixSuggestion.FileChange change : suggestion.changes()) {
                String filePath = change.filePath();
                String pathError = validateFilePath(filePath, allowedPathGlobs);
                if (pathError != null) {
                    listener.getLogger().println("[AutoFix] Skipping: " + pathError);
                    return AutoFixResult.skippedPathNotAllowed(filePath);
                }
            }
        }

        // Step 4 — Validate all diffs
        if (suggestion.changes() != null) {
            for (FixSuggestion.FileChange change : suggestion.changes()) {
                if ("modify".equals(change.action())) {
                    String diffError = UnifiedDiffApplier.validate(change.unifiedDiff());
                    if (diffError != null) {
                        String msg = "Invalid diff for " + change.filePath() + ": " + diffError;
                        listener.getLogger().println("[AutoFix] Failed: " + msg);
                        return AutoFixResult.failed(msg);
                    }
                }
            }
        }

        // Step 5 — Detect SCM remote URL and credentials
        String resolvedRemoteUrl = (remoteUrl != null && !remoteUrl.isBlank())
                ? remoteUrl : extractRemoteUrl(run);
        listener.getLogger().println("[AutoFix] SCM remote: " + resolvedRemoteUrl);

        String token = resolveScmToken(credentialsId, run);
        if (token == null) {
            return AutoFixResult.failed("SCM credentials not found for ID: " + credentialsId);
        }

        // Step 6 — Parse SCM repo (with enterprise overrides)
        ScmRepo repo = buildScmRepo(resolvedRemoteUrl, token, scmTypeOverride,
                githubEnterpriseUrl, gitlabUrl, bitbucketUrl);
        listener.getLogger().println("[AutoFix] SCM type: " + repo.scmType()
                + ", owner: " + repo.owner() + ", repo: " + repo.repoName());

        ScmApiClient client = ScmClientFactory.create(repo);

        // Step 7 — Validate credentials / write access
        listener.getLogger().println("[AutoFix] Validating write access...");
        client.validateWriteAccess();

        // Determine default branch
        String defaultBranch = client.getDefaultBranch();
        listener.getLogger().println("[AutoFix] Default branch: " + defaultBranch);

        // Step 8 — Create fix branch
        String branchName = "fix/jenkins-ai-" + run.getNumber() + "-" + System.currentTimeMillis();
        listener.getLogger().println("[AutoFix] Creating branch: " + branchName);
        client.createBranch(branchName, defaultBranch);
        createdBranchRef[0] = branchName;

        // Step 9 — Apply diffs, collect new file contents
        Map<String, String> fileContents = new LinkedHashMap<>();
        if (suggestion.changes() != null) {
            for (FixSuggestion.FileChange change : suggestion.changes()) {
                String filePath = change.filePath();
                listener.getLogger().println("[AutoFix] Preparing change for: " + filePath + " (" + change.action() + ")");

                String currentContent = client.getFileContent(filePath, defaultBranch);

                String newContent;
                if ("create".equals(change.action())) {
                    newContent = extractNewContent(change.unifiedDiff());
                } else {
                    // modify
                    if (currentContent == null) {
                        String msg = "Cannot modify non-existent file: " + filePath;
                        listener.getLogger().println("[AutoFix] Failed: " + msg);
                        rollbackBranch(client, branchName, listener);
                        return AutoFixResult.failed(msg);
                    }
                    try {
                        newContent = UnifiedDiffApplier.apply(currentContent, change.unifiedDiff());
                    } catch (IllegalArgumentException e) {
                        String msg = "Failed to apply diff to " + filePath + ": " + e.getMessage();
                        listener.getLogger().println("[AutoFix] Failed: " + msg);
                        rollbackBranch(client, branchName, listener);
                        return AutoFixResult.failed(msg);
                    }
                }
                fileContents.put(filePath, newContent);
            }
        }

        // Commit all changes atomically
        String commitMessage = "fix: AI auto-fix for build #" + run.getNumber();
        try {
            client.commitFiles(branchName, commitMessage, fileContents);
        } catch (IOException e) {
            String msg = "Failed to commit files: " + e.getMessage();
            listener.getLogger().println("[AutoFix] Failed: " + msg);
            rollbackBranch(client, branchName, listener);
            return AutoFixResult.failed(msg);
        }

        // Step 10 — Create PR
        String prTitle = "fix: AI auto-fix for " + run.getParent().getFullName() + " #" + run.getNumber();
        String effectiveTemplate = (prTemplate != null && !prTemplate.isBlank()) ? prTemplate : DEFAULT_PR_TEMPLATE;
        String prBody = buildPrBody(run, suggestion, effectiveTemplate);

        PullRequest pr;
        try {
            pr = client.createPullRequest(prTitle, prBody, branchName, defaultBranch, draftPr);
        } catch (IOException e) {
            String msg = "Failed to create pull request: " + e.getMessage();
            listener.getLogger().println("[AutoFix] Failed: " + msg);
            rollbackBranch(client, branchName, listener);
            return AutoFixResult.failed(msg);
        }

        listener.getLogger().println("[AutoFix] Pull request created: " + pr.url());

        // Step 11 — Store action and return
        AutoFixAction action = new AutoFixAction(
                AutoFixStatus.CREATED,
                pr.url(),
                branchName,
                "PR created: " + pr.url(),
                prTitle,
                System.currentTimeMillis(),
                repo.scmType().name());
        run.addOrReplaceAction(action);
        try {
            run.save();
        } catch (IOException e) {
            LOGGER.log(Level.WARNING, "Failed to persist AutoFixAction to build", e);
        }

        return AutoFixResult.created(pr.url(), branchName);
    }

