    String resolveScmToken(String credentialsId, Run<?, ?> run) {
        StringCredentials stringCredentials = CredentialsProvider.findCredentialById(
                credentialsId, StringCredentials.class, run, Collections.emptyList());
        if (stringCredentials != null) {
            return Secret.toString(stringCredentials.getSecret());
        }

        StandardUsernamePasswordCredentials usernamePasswordCredentials = CredentialsProvider.findCredentialById(
                credentialsId, StandardUsernamePasswordCredentials.class, run, Collections.emptyList());
        if (usernamePasswordCredentials != null) {
            return Secret.toString(usernamePasswordCredentials.getPassword());
        }

        return null;
    }

    /**
     * Parses a raw string from the AI (which may contain preamble) into a {@link FixSuggestion}.
     * Extracts the JSON block by finding the first '{' and last '}'.
     */
    FixSuggestion parseFixSuggestion(String rawJson) throws IOException {
        if (rawJson == null || rawJson.isBlank()) {
            throw new IOException("AI returned empty response");
        }
        int start = rawJson.indexOf('{');
        int end = rawJson.lastIndexOf('}');
        if (start < 0 || end < 0 || start >= end) {
            throw new IOException("No JSON object found in AI response: " + rawJson);
        }
        String jsonBlock = rawJson.substring(start, end + 1);
        return OBJECT_MAPPER.readValue(jsonBlock, FixSuggestion.class);
    }

    /**
     * Validates a file path against security constraints and the allowed-path globs.
     *
     * @return null if path is valid, or a description of why it was rejected
     */
    private String validateFilePath(String filePath, List<String> allowedPathGlobs) {
        if (filePath == null || filePath.isBlank()) {
            return "File path is null or blank";
        }
        if (filePath.startsWith("/")) {
            return "Absolute paths are not allowed: " + filePath;
        }
        // Normalize to catch trailing ".." segments (e.g. "src/main/..") that would
        // bypass a simple string-contains check. Also catches Windows-style "..\".
        Path normalized = Path.of(filePath).normalize();
        if (normalized.startsWith("..")) {
            return "Path traversal is not allowed: " + filePath;
        }
        if (allowedPathGlobs != null && !allowedPathGlobs.isEmpty()) {
            boolean matched = false;
            Path p = Path.of(filePath);
            for (String glob : allowedPathGlobs) {
                try {
                    PathMatcher matcher = FileSystems.getDefault().getPathMatcher("glob:" + glob);
                    Path fileName = p.getFileName();
                    if (matcher.matches(p) || (fileName != null && matcher.matches(fileName))) {
                        matched = true;
                        break;
                    }
                } catch (Exception e) {
                    LOGGER.warning("Invalid glob pattern '" + glob + "': " + e.getMessage());
                }
            }
            if (!matched) {
                return "File path '" + filePath + "' is not allowed by configured path patterns";
            }
        }
        return null;
    }

    /**
     * Extracts the remote URL from the run's SCM configuration via reflection (to avoid
     * a hard compile-time dependency on the git plugin).
     */
    String extractRemoteUrl(Run<?, ?> run) {
        SCM scm = extractConfiguredScm(run.getParent());
        if (scm == null) {
            throw new IllegalStateException("No SCM configured on this job");
        }
        return extractGitRemoteUrl(scm);
    }

    private SCM extractConfiguredScm(Item job) {
        if (job instanceof AbstractProject<?, ?> project) {
            return project.getScm();
        }

        SCM fromScmsMethod = extractFirstScmFromMethod(job, "getSCMs");
        if (fromScmsMethod != null) {
            return fromScmsMethod;
        }

        try {
            Method getDefinition = job.getClass().getMethod("getDefinition");
            Object definition = getDefinition.invoke(job);
            if (definition != null) {
                SCM fromDefinition = extractScmFromDefinition(definition);
                if (fromDefinition != null) {
                    return fromDefinition;
                }
            }
        } catch (NoSuchMethodException e) {
            LOGGER.fine("Job type does not expose getDefinition(): " + job.getClass().getName());
        } catch (ReflectiveOperationException e) {
            throw new IllegalStateException("Failed to inspect SCM definition for job type "
                    + job.getClass().getName() + ": " + e.getMessage(), e);
        }

        throw new IllegalStateException("Job type " + job.getClass().getName()
                + " does not support SCM URL extraction");
    }

    private SCM extractScmFromDefinition(Object definition) throws ReflectiveOperationException {
        SCM scm = extractScmFromMethod(definition, "getScm");
        if (scm != null) {
            return scm;
        }
        return extractFirstScmFromMethod(definition, "getSCMs");
    }

    private SCM extractScmFromMethod(Object target, String methodName) throws ReflectiveOperationException {
        try {
            Method method = target.getClass().getMethod(methodName);
            Object result = method.invoke(target);
            if (result instanceof SCM scm) {
                return scm;
            }
            return null;
        } catch (NoSuchMethodException e) {
            return null;
        }
    }

    private SCM extractFirstScmFromMethod(Object target, String methodName) {
        try {
            Method method = target.getClass().getMethod(methodName);
            Object result = method.invoke(target);
            if (result instanceof Collection<?> collection) {
                Iterator<?> iterator = collection.iterator();
                while (iterator.hasNext()) {
                    Object candidate = iterator.next();
                    if (candidate instanceof SCM scm) {
                        return scm;
                    }
                }
            }
            return null;
        } catch (NoSuchMethodException e) {
            return null;
        } catch (ReflectiveOperationException e) {
            throw new IllegalStateException("Failed to inspect SCMs via " + methodName + ": " + e.getMessage(), e);
        }
    }

    private String extractGitRemoteUrl(SCM scm) {
        String scmClassName = scm.getClass().getName();

        try {
            Method getRepositories = scm.getClass().getMethod("getRepositories");
            @SuppressWarnings("unchecked")
            List<?> repos = (List<?>) getRepositories.invoke(scm);
            if (!repos.isEmpty()) {
                Object repo = repos.get(0);
                Method getUrls = repo.getClass().getMethod("getURIs");
                @SuppressWarnings("unchecked")
                List<?> uris = (List<?>) getUrls.invoke(repo);
                if (!uris.isEmpty()) {
                    return uris.get(0).toString();
                }
            }
        } catch (NoSuchMethodException e) {
            throw new IllegalStateException(
                    "Unsupported SCM type: " + scmClassName + ". Only GitSCM is currently supported.");
        } catch (Exception e) {
            throw new IllegalStateException("Failed to extract remote URL from GitSCM: " + e.getMessage(), e);
        }
        throw new IllegalStateException("No Git remote URLs configured for SCM type: " + scmClassName);
    }

    /**
     * Builds a {@link ScmRepo} from the remote URL and applies any enterprise URL overrides.
     *
     * <p>When {@code scmTypeOverride} is specified, it bypasses URL-based host detection —
     * required for self-hosted instances (e.g. {@code git.company.com}) whose hostname
     * does not match {@code github.com}, {@code gitlab.com}, or {@code bitbucket.org}.
     */
    private ScmRepo buildScmRepo(String remoteUrl, String token, String scmTypeOverride,
                                  String githubEnterpriseUrl, String gitlabUrl, String bitbucketUrl) {
        // If scmTypeOverride is provided, use it directly so self-hosted instances work.
        if (scmTypeOverride != null && !scmTypeOverride.isBlank()) {
            ScmType forcedType;
            switch (scmTypeOverride.trim().toLowerCase()) {
                case "github":          forcedType = ScmType.GITHUB;          break;
                case "gitlab":          forcedType = ScmType.GITLAB;          break;
                case "bitbucket":       forcedType = ScmType.BITBUCKET;       break;
                case "bitbucketserver": forcedType = ScmType.BITBUCKET_SERVER; break;
                default:
                    LOGGER.warning("Unrecognised scmTypeOverride '" + scmTypeOverride
                            + "' — falling back to URL-based detection");
                    forcedType = null;
            }
            if (forcedType != null) {
                String baseUrl;
                switch (forcedType) {
                    case GITHUB:
                        baseUrl = (githubEnterpriseUrl != null && !githubEnterpriseUrl.isBlank())
                                ? githubEnterpriseUrl.stripTrailing() + "/api/v3"
                                : "https://api.github.com";
                        break;
                    case GITLAB:
                        baseUrl = (gitlabUrl != null && !gitlabUrl.isBlank())
                                ? gitlabUrl.stripTrailing() + "/api/v4"
                                : "https://gitlab.com/api/v4";
                        break;
                    case BITBUCKET:
                        baseUrl = resolveCloudBitbucketBaseUrl(bitbucketUrl);
                        break;
                    case BITBUCKET_SERVER:
                        if (bitbucketUrl == null || bitbucketUrl.isBlank()) {
                            throw new IllegalArgumentException(
                                    "autoFixBitbucketUrl must be set when scmTypeOverride is 'bitbucketserver'");
                        }
                        baseUrl = bitbucketUrl.stripTrailing() + "/rest/api/1.0";
                        break;
                    default:
                        baseUrl = null; // unreachable
                }
                return ScmRepo.parseWithOverride(remoteUrl, token, forcedType, baseUrl);
            }
        }

        ScmRepo repo = ScmRepo.parse(remoteUrl, token);

        // Apply enterprise base-URL overrides when the host was auto-detected
        if (repo.scmType() == ScmType.GITHUB && githubEnterpriseUrl != null && !githubEnterpriseUrl.isBlank()) {
            repo = repo.withBaseUrl(githubEnterpriseUrl.stripTrailing() + "/api/v3");
        } else if (repo.scmType() == ScmType.GITLAB && gitlabUrl != null && !gitlabUrl.isBlank()) {
            repo = repo.withBaseUrl(gitlabUrl.stripTrailing() + "/api/v4");
        } else if (repo.scmType() == ScmType.BITBUCKET && bitbucketUrl != null && !bitbucketUrl.isBlank()) {
            repo = repo.withBaseUrl(resolveCloudBitbucketBaseUrl(bitbucketUrl));
        } else if (repo.scmType() == ScmType.BITBUCKET_SERVER && bitbucketUrl != null && !bitbucketUrl.isBlank()) {
            repo = repo.withBaseUrl(bitbucketUrl.stripTrailing() + "/rest/api/1.0");
        }

        return repo;
    }

    String resolveCloudBitbucketBaseUrl(String bitbucketUrl) {
        if (bitbucketUrl == null || bitbucketUrl.isBlank()) {
            return "https://api.bitbucket.org/2.0";
        }

        String normalized = bitbucketUrl.stripTrailing();
        URI uri = URI.create(normalized);
        String host = uri.getHost();
        if (host == null) {
            throw new IllegalArgumentException("Bitbucket URL is not a valid absolute URL: " + bitbucketUrl);
        }

        if (normalized.endsWith("/2.0")) {
            return normalized;
        }
        return "https://api.bitbucket.org/2.0";
    }

    /**
     * Extracts the new file content from a unified diff (for "create" actions).
     * Collects all lines starting with '+' (excluding the '+++' header).
     */
    String extractNewContent(String unifiedDiff) {
        if (unifiedDiff == null || unifiedDiff.isBlank()) {
            return "";
        }
        StringBuilder sb = new StringBuilder();
        String[] lines = unifiedDiff.split("\r?\n", -1);
        boolean inHunk = false;
        for (String line : lines) {
            if (line.startsWith("@@ ")) {
                inHunk = true;
                continue;
            }
            if (line.startsWith("+++ ") || line.startsWith("--- ")) {
                continue;
            }
            if (inHunk && line.startsWith("+")) {
                if (sb.length() > 0) {
                    sb.append("\n");
                }
                sb.append(line.substring(1));
            }
        }
        return sb.toString();
    }

    /**
     * Builds a Markdown PR body by substituting {@code {variable}} placeholders in the template.
     */
    String buildPrBody(Run<?, ?> run, FixSuggestion suggestion, String template) {
        String jobName = run.getParent().getFullName();
        String buildNumber = String.valueOf(run.getNumber());
        String explanation = suggestion.explanation() != null ? suggestion.explanation() : "No explanation provided.";
        String confidence = suggestion.confidence() != null ? suggestion.confidence() : "unknown";
        String fixType = suggestion.fixType() != null ? suggestion.fixType() : "unknown";

        StringBuilder changesSummary = new StringBuilder();
        if (suggestion.changes() != null) {
            for (FixSuggestion.FileChange change : suggestion.changes()) {
                changesSummary.append("- **")
                        .append(change.filePath())
                        .append("** (")
                        .append(change.action())
                        .append("): ")
                        .append(change.description() != null ? change.description() : "")
                        .append("\n");
            }
        }
        if (changesSummary.isEmpty()) {
            changesSummary.append("No file changes.\n");
        }

        // Build values map and substitute all placeholders in a single pass so that
        // AI-provided content (explanation, descriptions) cannot match and corrupt
        // subsequent placeholder tokens (e.g. if explanation itself contains "{fixType}").
        Map<String, String> values = Map.of(
                "jobName", jobName,
                "buildNumber", buildNumber,
                "explanation", explanation,
                "changesSummary", changesSummary.toString().stripTrailing(),
                "fixType", fixType,
                "confidence", confidence
        );
        return TEMPLATE_PLACEHOLDER.matcher(template).replaceAll(m -> {
            String val = values.get(m.group(1));
            return val != null ? val : m.group(0);
        });
    }

    /**
     * Attempts to delete the given branch for rollback purposes. Logs but does not throw on failure.
     */
    private void rollbackBranch(ScmApiClient client, String branchName, TaskListener listener) {
        try {
            listener.getLogger().println("[AutoFix] Rolling back branch: " + branchName);
            client.deleteBranch(branchName);
        } catch (IOException e) {
            LOGGER.log(Level.WARNING, "Failed to delete branch during rollback: " + branchName, e);
        }
    }
